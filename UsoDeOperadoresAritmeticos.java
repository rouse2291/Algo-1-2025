public class UsoDeOperadoresAritmeticos {
    public static void main(String[] args) {

        int numero = 100;
        System.out.println(numero + " " + (numero + 5) + " " + (numero - 5) );
        System.out.println(numero + " " + (numero * 5) + " " + (numero / 5) );
        System.out.println(numero + " " + (numero % 5));
        System.out.println(++numero);
        System.out.println(--numero);


    }
    
}
